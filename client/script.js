function render(mode) {
    const container = document.getElementById('experience-container');
    if (!container) return;
    
    container.innerHTML = '';
    
    document.querySelectorAll('.toggle-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.mode === mode);
    });

    if (mode === 'experience') {
        renderExperience(container);
    } else if (mode === 'education') {
        renderEducation(container);
    } else if (mode === 'skills') {
        renderSkills(container);
    } else if (mode === 'contact') {
        renderContact(container);
    }
}

function renderExperience(container) {
    experiences.sort((a, b) => new Date(b.start_date) - new Date(a.start_date));
    experiences.forEach(exp => {
        const article = document.createElement('article');
        article.className = 'experience-item';
        
        let detailsHtml = exp.details.map((detail, idx) => {
            const parts = detail.split(':');
            const heading = parts[0].trim();
            const body = parts.length > 1 ? parts.slice(1).join(':').trim() : '';
            
            return `
                <div class="dropdown-item">
                    <h3 class="dropdown-trigger" onclick="toggleDropdown(this)">${heading} <span class="arrow">▼</span></h3>
                    <div class="dropdown-content">
                        <p>${body || 'Key responsibility and strategic contribution.'}</p>
                    </div>
                </div>
            `;
        }).join('');

        article.innerHTML = `
            <div class="exp-header">
                <h4>${exp.title}</h4>
                <span class="exp-period">${exp.period}</span>
            </div>
            <div class="exp-company">${exp.company}</div>
            <div class="exp-dropdowns">
                ${detailsHtml}
            </div>
        `;
        container.appendChild(article);
    });
}

